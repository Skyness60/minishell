#include "libft.h"

int	ft_isspace(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == ' ' || (str[i] >= 9 && str[i] <= 11))
			i++;
		else
			return (-1);
	}
	return (i);
}