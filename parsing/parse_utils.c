#include "../minishell.h"

bool	is_just_space(char *str)
{
	if (*str == 0)
		return (0);
	while (*str)
	{
		if (*str == ' ' || (*str < 9 && *str > 13))
			return (1);
		str++;
	}
	return (0);
}


